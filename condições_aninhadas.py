#Desafio 1-Escreva um programa para aprovar o empréstimo bancário para a compra
#de uma casa. O programa vai perguntar o valor da casa, o salário do comprador e
#em quantos anos ele vai pagar.
#Calcule o valor da prestação mensal, sanbendo que ela não pode exceder 30% do
#salário ou então empréstimo será negado.

valor_casa = float(input("Digite o valor do imovél: "))
salario = float(input("Informe o seu salário: "))
anos = int(input("Em quantos anos deseja pagar: "))

prestacao = valor_casa / (anos * 12)
if prestacao <= salario * 0.3:
    print("Empréstimo aprovado!")

else:
    print("Empréstimo negado!")

